console.log(4 > 3);
// OUTPUT : True

console.log(4 >= 3);
// OUTPUT : True

console.log(4 < 3);
// OUTPUT : False

console.log(4 <= 3);
// OUTPUT : False

console.log(4 == 4);
// OUTPUT : True

console.log(4 === 4);
// OUTPUT : True

console.log(4 != 4);
// OUTPUT : False

console.log(4 !== 4);
// OUTPUT : False

console.log(4 != "4");
// OUTPUT : False

console.log(4 == "4");
// OUTPUT : True

console.log(4 === "4");
// OUTPUT : False

